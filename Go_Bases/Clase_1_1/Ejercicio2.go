package main

import "fmt"

var Temperatura float32 = 18.5
var Humedad int = 60
var PresionAtmosferica float32 = 1013.8

func main() {
	fmt.Printf("Temperatura: %v Cº \n", Temperatura)
	fmt.Printf("Humedad: %v \n", Humedad)
	fmt.Printf("Presión Atmosferica: %v hPa \n", PresionAtmosferica)
}

